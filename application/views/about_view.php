<h2>About</h2>

<p>This project originally originated out of a common theme I noticed when chatting with friends about using Craigslist to buy / sell things.  For the more frugal / adventerous folks, using CL has been a blessing.  For the less adventurous folks, the anonymity and horror stories of using Craigslist prevented them from venturing into the wonderful world of bartering / reselling.   </p>

<p>This project aims to take the edge off that and build a community around 'bartering/selling/purchasing' with anonymity, and yet a baseline of credibility.</p>

<p>We hope you find this site useful.</p>